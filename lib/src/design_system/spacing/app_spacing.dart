/// {@template app_spacing}
/// A set of standardized spacing values for the app.
/// {@endtemplate}
abstract class AppSpacing {
  /// The default unit of spacing
  static const double spaceUnit = 4;

  /// x3s: 2
  static const double x3s = 0.5 * spaceUnit;

  /// xxs: 4
  static const double xxs = spaceUnit;

  /// xs: 8
  static const double xs = 2 * spaceUnit;

  /// sm: 12
  static const double sm = 3 * spaceUnit;

  /// md: 16
  static const double md = 4 * spaceUnit;

  /// lg: 20
  static const double lg = 5 * spaceUnit;

  /// xl: 24
  static const double xl = 6 * spaceUnit;

  /// xxl: 32
  static const double xxl = 8 * spaceUnit;

  /// xxxl: 40
  static const double xxxl = 10 * spaceUnit;
}
